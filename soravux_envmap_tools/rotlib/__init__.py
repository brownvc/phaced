def rotate(points, rotation_type, rotation, point=None):
    """
    Rotate `points` by `rotation` as defined by `rotation_type`.
    Available `rotation_type`s :
     - DCM: `rotation` must be a NxN matrix or (N+1)x(N+1) matrix for homogeneous coordinates
     - EA###": `rotation` is [psi, theta, phi]
     - EV: `rotation` is [x, y, z, theta] for a rotation of theta radians around vector [x, y, z] 
     - Q: `rotation` is the quaternion [w, x, y, z]

    inputs:
    :points: MxN numpy ndarray for M points of N dimensions
    :rotation_type: See above.
    :rotation: See above.
    """
    points = np.asarray(points)

    points = np.atleast_2d(points)

    R = rotationOperator(rotation_type, "DCM", rotation, point=point)

    # Handle homogeneous coordinates
    if (points.shape[1] + 1) == R.shape[0]:
        N = R.shape[0]
        points = np.hstack((points, np.ones((points.shape[0], 1))))
        result = points.dot(R)
        result = (result[:,:(N-1)].T / result[:,N-1]).T
    else:
        result = points.dot(R)

    return np.squeeze(result)


def rotationOperator(source_type, target_type, rotation, point=None):
    """Convert a rotation type to another one.

    To Do: pass through axes.
    """
    source_type = source_type.upper()
    target_type = target_type.upper()

    if source_type == target_type:
        R = rotation
    elif target_type == "DCM":
        if source_type == 'EV':
            R = EV2DCM(rotation[:3], rotation[3], point=point)
        elif source_type == 'Q':
            R = Q2DCM(rotation)
        elif source_type.startswith('EA'):
            R = EA2DCM(rotation[0], rotation[1], rotation[2], axes='s{}'.format(source_type[2:].lower()))
    elif target_type == 'Q':
        if source_type == 'EV':
            R = EV2Q(rotation[:3], rotation[3], point=point)
        elif source_type == 'DCM':
            R = DCM2Q(rotation)
        elif source_type.startswith('EA'):
            R = EA2Q(rotation[0], rotation[1], rotation[2], axes='s{}'.format(source_type[2:].lower()))
    elif target_type == 'EV':
        if source_type == 'Q':
            raise NotImplemented()
        elif source_type == 'DCM':
            R = DCM2EV(rotation)
        elif source_type.startswith('EA'):
            raise NotImplemented()
    elif target_type.startswith('EA'):
        if source_type == 'EV':
            raise NotImplemented()
        elif source_type == 'DCM':
            R = DCM2EA(rotation)
        elif source_type == 'Q':
            raise NotImplemented()
    else:
        raise Exception('Rotation type unknown: {}'.format(source_type))

    return R


## Generate pure-axis rotation matrices

def rot(theta):
    """
    Produces an counter-clockwise 2D rotation matrix with angle `theta` in radians.
    """
    return np.array([[np.cos(theta), -np.sin(theta)],
                     [np.sin(theta), np.cos(theta)]], dtype='float64')


def rotx(theta):
    """
    Produces a counter-clockwise 3D rotation matrix around axis X with angle `theta` in radians.
    """
    return np.array([[1, 0, 0],
                     [0, np.cos(theta), -np.sin(theta)],
                     [0, np.sin(theta), np.cos(theta)]], dtype='float64')


def roty(theta):
    """
    Produces a counter-clockwise 3D rotation matrix around axis Y with angle `theta` in radians.
    """
    return np.array([[np.cos(theta), 0, -np.sin(theta)],
                     [0, 1, 0],
                     [np.sin(theta), 0, np.cos(theta)]], dtype='float64')


def rotz(theta):
    """
    Produces a counter-clockwise 3D rotation matrix around axis Z with angle `theta` in radians.
    """
    return np.array([[np.cos(theta), -np.sin(theta), 0],
                     [np.sin(theta), np.cos(theta), 0],
                     [0, 0, 1]], dtype='float64')


